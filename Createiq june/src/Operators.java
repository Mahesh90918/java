
public class Operators {
	public static void main(String[] args) {
		int i1 = 20;
		int i2 = 10;
		System.out.println("addition: " + (i1 + i2));
		System.out.println("substraction: " + (i1 - i2));
		System.out.println("Multiplication: " + (i1 * i2));
		System.out.println("divison " + (i1 / i2)); // 20/10==> 2-> co-efficient
		System.out.println("module: " + (i1 % i2)); // reminder
	}

}
