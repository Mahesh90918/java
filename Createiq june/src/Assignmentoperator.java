
public class Assignmentoperator {
	public static void main(String[] args)
	{
	int a=10;
	int b=15;
	int c=3;
	int d=4;
	int e=10;
	a+=4;//a=a+10=4+10=14
	b-=5;//b=b-5=15-5=10
	c*=3;//c=c*3=9
	d/=2;// d=d/2=4/2=2	
	e%=3;//e=e/3=10/3=1 (remainder value taken)in this modulomultiplication condition
	System.out.println(a);
	System.out.println(b);
	System.out.println(c);
	System.out.println(d);
	System.out.println(e);
	}
}
