package com.java;

public class Alphabets {
	public static void main(String[] args) {
	char A='9';
	if((A>='a'&&A<='z')||(A<='A'&&A>='Z'))
	{
		System.out.println("character");
	}else if(A>='0'&&A<='9')
	{
		System.out.println("numbers");
		
	}else {
		System.out.println("special characters");
	}
	}

}
