package com.basicprogrames;

public class Ifelse2 {

	public static void main(String[] args) {
		double a= 2;
		if (a>=1 && a<=5) {
			System.out.println( " 1to5");
		}if(a>=6 && a<=10) {
			System.out.println("6to10");
		}if(a>=11 && a<=20) {
			System.out.println("11to20");
		}if(a>=-1 && a<=-20) {
			System.out.println("-1to20");
		}
		
	}
}
